print("Programa para Ordenar Números Enteros y que los Imprima en Orden Descendente: ")

n1 = int(input("Ingrese un Primer Número Entero: "))
n2 = int(input("Ingrese un Segundo Número Entero:  "))

if ( n1 == n2 ):
    print ("Son Iguales")

elif ( n1 > n2 ):
    print (n2)
    print (n1)
else:
    print (n1)
    print (n2)
