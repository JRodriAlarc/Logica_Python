print("Programa para Calcular el Perimetro de un Rectangulo y un Triangulo: ")
print('Para Calcular el Perimetro de Un Rectangulo: ')

L = float(input('Ingrese el Largo del Rectangulo: '))
A = float(input('Ingrese el Ancho del Rectangulo: '))

R1 = (2*L)+(2*A)

print('  - El Perimetro del Rectangulo es: %s' % R1)
print('')

print('Para Calcular el Perimetro de Un Triangulo: ')

L1 = float(input('Ingrese el tamaño del Primer Lado: '))
L2 = float(input('Ingrese el tamaño del Segundo Lado: '))
L3 = float(input('Ingrese el tamaño del Tercer Lado: '))

R2 = (L1+L2+L3)

print('  - El perimetro del Triangulo es: %s' % R2)